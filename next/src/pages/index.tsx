import type { NextPage } from "next";
import Head from "next/head";
import { useState } from "react";
import Button, { ButtonProps } from "~/components/Button";

type ButtonHandler = React.MouseEventHandler<HTMLButtonElement>

const Calculator: NextPage = () => {
  const [display, setDisplay] = useState("");
  const [clearOnNum, setClearOnNum] = useState(false);
  const [firstOperand, setFirstOperand] = useState("");
  const [secondOperand, setSecondOperand] = useState("");
  const [whichOperand, setWhichOperand] = useState(0);    // first operand
  const [operation, setOperation] = useState("");
  const [float, setFloat] = useState(false);

  const handleNumClick: ButtonHandler = (e) => {
    if (clearOnNum) {
      setDisplay(e.currentTarget.textContent || "");
      setFloat(false);
      setClearOnNum(false);
    } else {
      setDisplay(display + e.currentTarget.textContent)
    }
  }

  const NumButton = ({ className, children }: ButtonProps) => {
    return <Button onClick={handleNumClick} className={className}>{children}</Button>
  }

  const handleOpClick: ButtonHandler = (e) => {
    if (whichOperand === 0) {
      setOperation(e.currentTarget.textContent || "");
      setFirstOperand(display);
      setClearOnNum(true);
      console.log("should clear on next number")
      setWhichOperand(1);
      setFloat(false);
    } else {
      setOperation(e.currentTarget.textContent || "");
    }
  }

  const OpButton = ({ children }: ButtonProps) => {
    return <Button onClick={handleOpClick} className="bg-orange-400 hover:bg-orange-300">{children}</Button>
  }

  const handleDotClick: ButtonHandler = () => {
    if (float) {
      return;
    } else if (clearOnNum) {
      setDisplay("0.");
      setFloat(true);
    } else {
      setDisplay(display + ".");
      setFloat(true);
    }
  }

  const DotButton = ({ className = "" }) => {
    return <Button onClick={handleDotClick} className={className}>.</Button>
  }

  const handleClearClick: ButtonHandler = () => {
    setDisplay("");
    setFloat(false);
  }

  const ClearButton = ({ className = "" }) => {
    return <Button onClick={handleClearClick} className={className}>C</Button>
  }

  const handleEqualsClick: ButtonHandler = () => {
    if (operation === "") return;

    let route = "";
    switch (true) {
      case operation === "+" || operation === "add":
        route = "add";
        break;
      case operation === "x" || operation === "*" || operation === "mul":
        route = "mul";
        break;
      case operation === "/" || operation === "div":
        route = "div";
        break;
      case operation === "-" || operation === "sub":
        route = "sub";
        break;
    }

    const params = new URLSearchParams({
      x: firstOperand,
      y: secondOperand === "" ? firstOperand : secondOperand,
    });

    fetch(`/api/form/${route}`, { method: "POST", body: params })
      .then(res => res.text())
      .then(data => setDisplay(data));
  }

  const EqualsButton = ({ className = "" }) => {
    return <Button onClick={handleEqualsClick} className={`bg-orange-400 hover:bg-orange-300 ${className}`}>=</Button>
  }

  return (
    <>
      <Head>
        <title>Calculator</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div className="flex h-screen w-screen items-center justify-items-center font-mono">
        <div className="mx-auto w-96">
          <div className="grid grid-cols-4 gap-0 drop-shadow-lg">
            <h1 className="text-right text-white bg-neutral-500 p-4 text-2xl col-span-4 rounded-t-lg">
              {display === "" ? "0" : display}
            </h1>
            <ClearButton className="col-span-3" />
            <OpButton>/</OpButton>
            <NumButton>7</NumButton>
            <NumButton>8</NumButton>
            <NumButton>9</NumButton>
            <OpButton>x</OpButton>
            <NumButton>4</NumButton>
            <NumButton>5</NumButton>
            <NumButton>6</NumButton>
            <OpButton>-</OpButton>
            <NumButton>1</NumButton>
            <NumButton>2</NumButton>
            <NumButton>3</NumButton>
            <OpButton>+</OpButton>
            <NumButton className="col-span-2 rounded-bl-lg">0</NumButton>
            <DotButton />
            <EqualsButton className="rounded-br-lg" />
          </div>
        </div>
      </div>
    </>
  );
};

export default Calculator;
